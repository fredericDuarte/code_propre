package ex1;

/**
 * Classe qui réalise des opérations arythmétiques
 * @author DIGINAMIC
 */
public class Calcul {

	/** Méthode qui retourne l'addition des paramètres a et b 
	 * 
	 * @param a
	 * @param b
	 * @return int 
	 */
	public int addition(int a, int b){
		return a+b;
	}
	
	/**
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public int soustraction(int a, int b){
		return a-b;
	}
}
